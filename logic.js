window.onload = () => {
	let bingoToken = document.getElementById("bingo-token");
	bingoToken.innerHTML = "<span class='english'>Hi</span>";

	let bingoInfo = document.getElementById("bingo-token-info");
	bingoInfo.innerHTML = "<span class='info english'>Select a character set, then</span><span class='info english'>click 'New Game' start a game.</span>";

	document.getElementById("new-game").onclick = newGame;
	document.getElementById("refresh").onclick = getItem;

	let items, current, type;

	function newGame() {
		type = document.getElementById("character-set").value;
		if(type == "kanji"){
			items = shuffle(kanji);
		} else if(type == "hiragana"){
			items = shuffle(hiragana);
		} else {
			items = shuffle(katakana);
		}
		current = 0;
	}

	function shuffle(arr){
		let count = arr.length;
		let index, temp;

		while(count > 0){
			index = Math.floor(Math.random()*count);
			count--;

			temp = arr[count];
			arr[count] = arr[index];
			arr[index] = temp;
		}
		return arr;
	}

	function getItem() {
		if(current == items.length) {
			// end of array
			document.getElementById("bingo-token").innerHTML = "";
			document.getElementById("bingo-token-info").innerHTML = "<span class='info'>End of data!</span>";
			newGame();
			return;
		}
		current++;
		if(type == "kanji"){
			displayKanji(items[current]);
		}
		if(type == "hiragana" || type == "katakana") {
			displayKana(items[current]);
		}
	}

	function displayKanji(item){
		document.getElementById("bingo-token").innerHTML = item.kanji;
		document.getElementById("bingo-token-info").innerHTML = "<span class='info english'>Meaning: " + item.meaning + "</span><span class='info'>訓読み: " + item.kunyomi + "</span><span class='info'>音読み: " + item.onyomi + "</span>";
	}

	function displayKana(item){
		document.getElementById("bingo-token").innerHTML = item.item;
		document.getElementById("bingo-token-info").innerHTML = item.info ? item.info : "";
	}
}
